public class base8 {

    public static void main(String[] args) {
        for (int i = 1; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print(i);
                System.out.print(j);
                System.out.print(j);
                System.out.print(i);
                System.out.println();
            }

        }
    }

}
